public class Chpt2Number4
{


    public static void main(String[] args)
    {
		char one;

		one = '*';
		// 1
        System.out.println("   " + one);
		// 3
        System.out.println("  " + one + "*" + one);
		// 5
        System.out.println(" " + one + "*" + one + "*" + one);
        // 7
        System.out.println(one + "*" + one + "*" + one + "*" + one);
        // 5
        System.out.println(" " + one + "*" + one + "*" + one);
        // 3
        System.out.println("  " + one + "*" + one);
        // 1
        System.out.println("   " + one);
    }

}